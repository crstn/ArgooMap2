package com.mapitz.gwt.googleMaps.client;

public class GLog extends GoogleMapsWidget {

    protected GLog(JSObject element) {
        super(element);
    }

    /**
     * write(message, color?) none Writes the message as plain text into the log
     * window. HTML markup characters will be escaped so that they are visible
     * as characters.
     * 
     * @param message
     * @param color
     */
    public static void write(String message, String color) {
        GLogImpl.write(message, color);
    }

    /**
     * write(message, color?) none Writes the message as plain text into the log
     * window. HTML markup characters will be escaped so that they are visible
     * as characters.
     * 
     * @param message
     * @param color
     */
    public static void write(String message) {
        GLogImpl.write(message);
    }

    /**
     * writeUrl(url) none Writes a link to the given URL into the log window.
     * 
     * @param url
     */
    public static void writeUrl(String url) {
        GLogImpl.writeUrl(url);
    }

    /**
     * writeHtml(html) none Writes text as HTML in the log window.
     * 
     * @param html
     */
    public static void writeHtml(String html) {
        GLogImpl.writeHtml(html);
    }

}
